public class Matematik {
    protected int angka1;
    protected int angka2;
    
    public int pengurangan() {
        return angka1 - angka2;
    }
    
    public int pertambahan() {
        return angka1 + angka2;
    }
    
    public int modulus() {
        return angka1 % angka2;
    }
}